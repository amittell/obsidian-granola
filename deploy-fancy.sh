# Animated spinner
spinner() {
    local pid=$1
    local delay=0.1
    local spinstr='|/-\'
    while [ "$(ps a | awk '{print $1}' | grep $pid)" ]; do
        local temp=${spinstr#?}
        printf " [${CYAN}%c${NC}]  " "$spinstr"
        local spinstr=$temp${spinstr%"$temp"}
        sleep $delay
        printf "\b\b\b\b\b\b"
    done
    printf "    \b\b\b\b"
}

# Rainbow text effect
rainbow() {
    local text="$1"
    local colors=(196 208 226 46 21 93)
    local i=0
    for ((j=0; j<${#text}; j++)); do
        printf "\033[38;5;${colors[$((i % 6))]}m${text:$j:1}"
        ((i++))
    done
    printf "${NC}"
}

# Typewriter effect
typewriter() {
    local text="$1"
    local delay=${2:-0.03}
    for ((i=0; i<${#text}; i++)); do
        printf "${text:$i:1}"
        sleep "$delay"
    done
    echo
}

# Glitch effect
glitch() {
    local text="$1"
    for i in {1..3}; do
